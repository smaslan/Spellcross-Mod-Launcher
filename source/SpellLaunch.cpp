// make SPELCROS.CD file with drive letter
int SpellLaunch::MakeSpellCD(std::filesystem::path spell_dir,std::string drive)
{
    if(drive.size() != 1)
        return(1);
    auto path = spell_dir / "SPELCROS.CD";
    std::string old;
    if(loadstr(path.wstring(),old) || old != drive)
        savestr(path.wstring(),toupper(drive));
    return(0);
}

// make DOSbox batch file: the one to be executed within DOSbox to launch the game setup
int SpellLaunch::MakeSpellDOSboxSetupBat(std::filesystem::path spell_dir)
{
    auto path = spell_dir / box_setup_name;
    std::string bat;
    bat += string_format("@ECHO OFF\n\n");
    bat += string_format("rem --- Script to run Spellcross SETUP inside DOSbox ---\n");
    bat += string_format("rem --- (autogenerated by Spellcross Mod Launcher)   ---\n\n");

    bat += string_format("rem --- run Spellcross SETUP ---\n");
    bat += string_format("@ECHO Starting Spellcross SETUP ...\n");
    bat += string_format("CALL SETUP.BAT\n\n");

    bat += string_format("rem--- finito---\n");
    bat += string_format("exit\n");

    std::string bat_old;
    if(loadstr(path,bat_old) || bat_old != bat)
        savestr(path,bat);

    return(0);
}

// make DOSbox batch file: the one to be executed within DOSbox to launch the game
int SpellLaunch::MakeSpellDOSboxExecBat(std::filesystem::path spell_dir,std::filesystem::path spellcd_dir,std::string drive,std::string spell_exe)
{
    auto path = spell_dir / box_exec_name;
    std::string bat;
    bat += string_format("@ECHO OFF\n\n");
    bat += string_format("rem --- Script to run Spellcross game inside DOSbox ---\n");
    bat += string_format("rem --- (autogenerated by Spellcross Mod Launcher)  ---\n\n");

    bat += string_format("rem --- Spellcross executable name ---\n");
    bat += string_format("SET EXENAME=%s\n\n",spell_exe.c_str());

    // mount CD optionally only if CD path defined
    if(!spellcd_dir.empty())
    {
        if(drive.size() != 1)
            return(1);
        bat += string_format("rem--- Spellcross CD content folder---\n");
        bat += string_format("SET SPELLCD=%ls\n\n",spellcd_dir.wstring().c_str());

        bat += string_format("rem--- mount Spellcross CD drive from folder---\n");
        bat += string_format("@ECHO Mounting virtual CD drive %s ...\n",drive.c_str());
        bat += string_format("mount %s \"%%SPELLCD%%\" -t cdrom\n\n",drive.c_str());
    }

    bat += string_format("rem--- run Spellcross---\n");
    bat += string_format("@ECHO Starting Spellcross ...\n");
    bat += string_format("%%EXENAME%%\n\n");

    bat += string_format("rem--- finito---\n");
    bat += string_format("exit\n");

    std::string bat_old;
    if(loadstr(path,bat_old) || bat_old != bat)
        savestr(path,bat);

    return(0);
}

// make DOSbox batch file: the one that starts DOSbox and launches the game inside
int SpellLaunch::MakeSpellDOSboxRunBat(std::filesystem::path spell_dir,std::filesystem::path dosbox_path,bool no_autoexec,bool fullscreen)
{
    auto path = spell_dir / run_dosbox_name;
    std::string bat;
    bat += string_format("@ECHO OFF\n\n");
    bat += string_format("rem --- Script to start DOSbox and run Spellcross game inside ---\n");
    bat += string_format("rem --- (autogenerated by Spellcross Mod Launcher)            ---\n\n");

    bat += string_format("rem --- DOXbox path ---\n");
    bat += string_format("SET DOSBOX=%ls\n\n",dosbox_path.wstring().c_str());

    bat += string_format("rem --- Launch Spellcross through DOSbox ---\n");
    bat += string_format("@ECHO Starting Spellcross in DOSbox ...\n");
    bat += string_format("cmd /C \"%%DOSBOX%%\" %s",box_exec_name.c_str());
    if(no_autoexec)
        bat += " -noautoexec";
    if(fullscreen)
        bat += " -fullscreen";
    bat += "\n";

    std::string bat_old;
    if(loadstr(path,bat_old) || bat_old != bat)
        savestr(path,bat);

    return(0);
}

