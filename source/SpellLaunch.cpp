// make Win32 batch file: the one that starts DOSbox and launches the game inside
int SpellLaunch::MakeSpellWin32RunBat(std::filesystem::path spell_dir,std::filesystem::path spellcd_dir,std::string drive,std::string spell_exe)
{
    auto path = spell_dir / run_win32_name;
    std::string bat;
    bat += string_format("@ECHO OFF\n\n");
    bat += string_format("rem --- Script to run Spellcross on Win32 natively ---\n");
    bat += string_format("rem --- (autogenerated by Spellcross Mod Launcher) ---\n\n");

    bat += string_format("rem --- Spellcross executable name ---\n");
    bat += string_format("SET EXENAME=%s\n\n",spell_exe.c_str());

    if(!spellcd_dir.empty())
    {
        if(drive.empty())
            return(1);

        bat += string_format("rem --- Spellcross CD content folder ---\n");
        bat += string_format("SET SPELLCD=%ls\n\n",spellcd_dir.wstring().c_str());

        bat += string_format("rem --- Create virtual drive from Spellcross CD folder ---\n");
        bat += string_format("@ECHO Mounting virtual drive %s ...\n",drive.c_str());
        bat += string_format("subst %s: \"%%SPELLCD%%\"\n\n",drive.c_str());
    }

    bat += string_format("rem --- Run Spellcross---\n");
    bat += string_format("@ECHO Starting Spellcross ...\n");
    bat += string_format("IF(%%OS%%)==(Windows_NT) GOTO WinNT\n");
    bat += string_format("START /WAIT command /C %%EXENAME%%\n");
    bat += string_format("GOTO Win98\n");
    bat += string_format(":WinNT\n");
    bat += string_format("START \"Spellcross Launcher by Stanislav Maslan\" /D \"%%CD%%\" /WAIT \"cmd /C \"%%EXENAME%%\" \"\n");
    bat += string_format(":Win98\n\n");

    if(!spellcd_dir.empty())
    {
        bat += string_format("rem --- Unmount virtual drive ---\n");
        bat += string_format("@ECHO Unmounting virtual drive %s ...\n",drive.c_str());
        bat += string_format("subst %s: /D\n",drive.c_str());
    }

    std::string bat_old;
    if(loadstr(path,bat_old) || bat_old != bat)
        savestr(path,bat);

    return(0);
}
