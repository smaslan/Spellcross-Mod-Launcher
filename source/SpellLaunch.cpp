const std::string SpellLaunch::setup_win32_name = "SETUP.bat";

// run game
int SpellLaunch::RunGame(std::filesystem::path spell_dir, std::string run_mode, std::function<void(std::string)> console_cb)
{    
    if(console_cb)
        console_cb("Running game ... \n");

    // select run mode
    std::string batch_name = "";
    if(run_mode == "DOSbox")
        batch_name = run_dosbox_name;
    else if(run_mode == "Win32")
        batch_name = run_win32_name;
    if(batch_name.empty())
    {
        if(console_cb)
            console_cb(" - failed! Unknown run mode.\n");
        return(1);
    }

    // get spellcross path
    if(spell_dir.empty())
    {
        if(console_cb)
            console_cb(" - failed! Spellcross directory not selected.\n");
        return(1);
    }
    auto batch_path = spell_dir / std::filesystem::path(batch_name);

    //std::string command = string_format("START \"Spellcross launcher\" /D \"%ls\" /WAIT \"cmd /C \"%s\" \"",spell_dir.c_str(), batch_name.c_str());
    std::string command = string_format("START \"Spellcross Mod Build by Stanislav Maslan\" /D \"%ls\" /WAIT \"%s\"",spell_dir.c_str(),batch_name.c_str());
    std::system(command.c_str());

    if(console_cb)
        console_cb(" - Done!\n");
    
    return(0);
}

